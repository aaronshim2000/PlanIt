package com.example;

public class User
{
    private String user;
    private String password;

    public String getUser()
    {
        return this.user;
    }

    public String getPassword()
    {
        return this.password;
    }

    public void setUser(String s)
    {
        user = s;
    }

    public void setPassword(String s)
    {
        password = s;
    }
}